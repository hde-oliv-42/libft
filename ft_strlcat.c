#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	unsigned int	i;
	unsigned int	j;
	unsigned int	org_size;

	j = 0;
	org_size = ft_strlen(dst);
	i = org_size;
	if (org_size >= size)
		return (ft_strlen(src) + size);
	while (i < (size - 1) && src[j])
	{
		dst[i] = src[j];
		i++;
		j++;
	}
	dst[i] = '\0';
	return (ft_strlen(src) + org_size);
}
